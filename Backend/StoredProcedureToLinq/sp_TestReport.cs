    public static IQueryable<sp_TestReportResult> Query(TradeNetDbContext db)
    {
        ArgumentNullException.ThrowIfNull(db);

        return
            from member in db.Members
            join paThaKa in db.PaThaKas on member.Id equals paThaKa.MemberId
            where CompanyRegistrationNumbers.Contains(paThaKa.CompanyRegistrationNo)
            select new sp_TestReportResult
            {
                CompanyRegistrationNo = paThaKa.CompanyRegistrationNo,
                MOCStatus = paThaKa.Mocstatus,
                CompanyName = paThaKa.CompanyName,
                Address = paThaKa.UnitLevel == null
                    ? null
                    : paThaKa.UnitLevel
                        + " "
                        + paThaKa.StreetNumberStreetName
                        + " "
                        + paThaKa.QuarterCityTownship
                        + paThaKa.State
                        + " "
                        + paThaKa.Country,
                MemberEmail = member.Email,
                Password = member.Password,
                MemberCode = member.MemberCode,
                MemberExpiredDate = member.EndDate
            };
    }
